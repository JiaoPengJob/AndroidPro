package com.tch.zx.util;

/**
 * EventBus消息事件类
 */

public class MessageEvent {

    private int message = 0;

    public MessageEvent(int message) {
        this.message = message;
    }

    public int getMessage() {
        return message;
    }

    public void setMessage(int message) {
        this.message = message;
    }

}
